// Exemples de manipulation de nombres
// Opérations arithmétiques
console.log(2);
console.log(10 + 5); // 15
console.log(99.99 * 1.15);
console.log(100 - 20);
console.log(200 / 4);

// Opérations booléennes (très simplement dit: comparaison)
console.log(100 > 90); // true
console.log(10 > 90); // false
console.log(50 <= 50); // true
console.log(10 < 20); // true
console.log(2 == 3); // false
console.log(2 != 3); // true
console.log(2 != 2); // false

// Valeurs booléennes
console.log(true);
console.log(false);